#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	char	*ptr;
	size_t	i;

	i = 0;
	ptr = (char *)malloc(count * size);
	if (!ptr || SIZE_MAX < count * size)
		return (NULL);
	ft_memset(ptr, 0, count * size);
	return ((void *)ptr);
}
